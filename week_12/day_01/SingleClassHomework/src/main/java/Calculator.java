public class Calculator {

    int calcAdd(int num1, int num2) {
        return num1 + num2;
    }
    int calcSubtract(int num1, int num2) {
        return num1 - num2;
    }
    int calcMultiply(int num1, int num2) {
        return num1 * num2;
    }

    double calcDivide(double num1, double num2) {
        return num1 / num2;
    }
}
